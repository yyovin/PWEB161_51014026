<center><h1> Jalan Mulia Berunsur Delapan</h1></center>
<hr>

	<p><h2><ol>
Dalam Dhammacakkappavattana Sutta; Samyutta Nikaya, Guru Buddha mengajarkan Empat Kebenaran Ariya kepada Lima Bhikkhu Pertama (Panca Vaggiya Bhikkhu), yang di dalamnya terdapat Jalan yang Menuju Terhentinya Dukkha. Jalan itu disebut dengan Jalan Mulia Berunsur Delapan (Ariya Atthangiko Magga). Di dalam Jalan ini mengandung unsur sila (kemoralan), samadhi (konsentrasi), dan panna (kebijaksanaan).<br><br>
Jalan Mulia Berunsur Delapan dibabarkan sebagai berikut :<br>
<li> Pengertian Benar (Sammã Ditthi)</li>
Pemahaman Benar adalah pengetahuan yang disertai dengan penembusan terhadap<br>
    a. Empat Kebenaran Mulia<br>
    b. Hukum Tilakkhana (Tiga Corak Umum)<br>
    c. Hukum Paticca-Samuppäda<br>
    d. Hukum Kamma<br>
<br>
<li> Pikiran Benar (Sammã Sankappa)</li>
Pikiran Benar adalah pikiran yang bebas dari:<br>
    a. Pikiran yang bebas dari nafsu-nafsu keduniawian (nekkhamma-sankappa)<br>
    b. Pikiran yang bebas dari kebencian (avyäpäda-sankappa)<br>
    c. Pikiran yang bebas dari kekejaman (avihimsä-sankappa)<br>
<br>
<li> Ucapan Benar (Sammã Vãca)</li>
Ucapan Benar adalah berusaha menahan diri dari berbohong (musãvãdã), memfitnah (pisunãvãcã), berucap kasar/caci maki (pharusavãcã), dan percakapan-percakapan yang tidak bermanfaat/pergunjingan (samphappalãpã). Dapat dinamakan Ucapan Benar, jika dapat memenuhi empat syarat di bawah ini :<br>
    a. Ucapan itu benar<br>
    b. Ucapan itu beralasan<br>
    c. Ucapan itu berfaedah<br>
    d. Ucapan itu tepat pada waktunya<br>
<br>
<li> Perbuatan Benar (Sammã Kammantã)</li>
Perbuatan Benar adalah berusaha menahan diri dari pembunuhan, pencurian, perbuatan melakukan perbuatan seksualitas yang tidak dibenarkan (asusila), perkataan tidak benar, dan penggunaan cairan atau obat-obatan yang menimbulkan ketagihan dan melemahkan kesadaran.<br>
<br>
<li> Penghidupan Benar (Sammã Ãjiva)</li>
Penghidupan Benar berarti menghindarkan diri dari bermata pencaharian yang menyebabkan kerugian atau penderitaan makhluk lain. "Terdapat lima objek perdagangan yang seharusnya dihindari (Anguttara Nikaya, III, 153), yaitu:<br>
    a. makhluk hidup<br>
    b. senjata<br>
    c. daging atau segala sesuatu yang berasal dari penganiayaan mahluk-mahluk hidup<br>
    d. minum-minuman yang memabukkan atau yang dapat menimbulkan ketagihan,<br>
    e. racun<br>
<br>
Dan terdapat pula lima pencaharian salah yang harus dihindari (Majjima Nikaya. 117), yaitu:<br>
    a. Penipuan<br>
    b. Ketidaksetiaan<br>
    c. Penujuman<br>
    d. Kecurangan<br>
    e. Memungut bunga yang tinggi (praktik lintah darat)<br>
<br>
<li> Usaha Benar (Sammã Vãyama)</li>
Usaha Benar dapat diwujudkan dalam empat bentuk tindakan, yaitu: berusaha mencegah munculnya kejahatan baru, berusaha menghancurkan kejahatan yang sudah ada, berusaha mengembangkan kebaikan yang belum muncul, berusaha memajukan kebaikan yang telah ada.<br>
<br>
<li> Perhatian Benar (Sammã Sati)</li>
Perhatian Benar dapat diwujudkan dalam empat bentuk tindakan, yaitu:<br>
    - perhatian penuh terhadap badan jasmani (kãyãnupassanã)<br>
    - perhatian penuh terhadap perasaan (vedanãnupassanã)<br>
    - perhatian penuh terhadap pikiran (cittanupassanã)<br>
    - perhatian penuh terhadap mental/batin (dhammanupassanã)<br>
Keempat bentuk tindakan tersebut bisa disebut sebagai Vipassanã Bhãvanã.<br>
<br>
<li> Konsentrasi Benar (Sammã Samãdhi)</li>
Konsentrasi Benar berarti pemusatan pikiran pada objek yang tepat sehingga batin mencapai suatu keadaan yang lebih tinggi dan lebih dalam.<br>
	</h2></ol></p>

