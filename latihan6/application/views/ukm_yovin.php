<center><h1>UKM BODHIVIJJA</h1></center>
<hr>
<pre>

<h2> VISI
Membentuk karakter pemuda-pemudi Buddhis yang unggul dan dapat mengembangkan Buddha Dhamma dalam setiap perbuatan baik didalam maupun diluar kampus, serta dapat berkontribusi nyata bagi masyarakat.</h2>

<h2> MISI
<li>Menjalin dan meningkatkan keakraban antar sesama anggota seDhamma.</li>
<li>Membina keyakinan terhadap Buddha Dhamma.</li>
<li>Mengedepankan sikap disiplin, mandiri, dan bertanggung jawab dalam mengembangkanTriDharma.</li>
<li>Melaksanakan kegiatan-kegiatan terkait keagamaan maupun kegiatan sosial lainnya.</li>
</h2>

