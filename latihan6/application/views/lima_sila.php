<center><h1> Lima Sila <em>(Pancasila)</em> dalam Agama Buddha </h1></center>
<hr>

	<p><h2><ul>
Nilai-nilai kemoralan yang diharuskan untuk umat awam umat Buddha biasanya dikenal dengan Pancasila. <br>
Kelima nilai-nilai kemoralan untuk umat awam adalah : <br>
 <li>Panatipata Veramani Sikkhapadam Samadiyami </li>
 <li>Adinnadana Veramani Sikkhapadam Samadiyami </li>
 <li>Kamesu Micchacara Veramani Sikhapadam </li>
 <li>Musavada Veramani Sikkhapadam Samadiyami </li>
 <li>Surameraya Majjapamadatthana Veramani Sikkhapadam Samadiyami </li>
<br>
Yang artinya:<br>
 <li>Aku bertekad akan melatih diri menghindari pembunuhan makhluk hidup. </li>
 <li>Aku bertekad akan melatih diri menghindari pencurian/mengambil barang yang tidak diberikan. </li>
 <li>Aku bertekad akan melatih diri menghindari melakukan perbuatan asusila. </li>
 <li>Aku bertekad akan melatih diri menghindari melakukan perkataan dusta. </li>
 <li>Aku bertekad akan melatih diri menghindari makanan atau minuman yang dapat menyebabkan lemah kesadaran. </li>

	</h2></ul></p>

