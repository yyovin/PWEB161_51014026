		<li><a href="https://id.wikipedia.org/wiki/Waisak" target="_blank">Hari Tri Suci Waisak</a></li>
		<center><img src="../img/waisak.jpg"> </center> <br>
Waisak atau Waisaka (Pali; Sanskrit: Vaiśākha वैशाख) merupakan hari suci agama Buddha. Hari Waisak juga dikenal dengan nama Visakah Puja atau Buddha Purnima di India, Saga Dawa di Tibet, Vesak di Malaysia, dan Singapura, Visakha Bucha di Thailand, dan Vesak di Sri Lanka. Nama ini diambil dari bahasa Pali "Wesakha", yang pada gilirannya juga terkait dengan "Waishakha" dari bahasa Sanskerta. Di beberapa tempat disebut juga sebagai "hari Buddha".<br>
<br>
Dirayakan dalam bulan Mei pada waktu terang bulan (purnama sidhi) untuk memperingati 3 (tiga) peristiwa penting, yaitu :<br>
	1. Lahirnya Pangeran Siddharta di Taman Lumbini pada tahun 623 S.M.<br>
   	2. Pangeran Siddharta mencapai Penerangan Agung dan menjadi Buddha di Buddha-Gaya (Bodhgaya) pada usia 35 tahun pada tahun 588 S.M.<br>
   	3. Buddha Gautama parinibbana (wafat) di Kusinara pada usia 80 tahun pada tahun 543 S.M.<br>
<br>
Tiga peristiwa ini dinamakan "Trisuci Waisak". Keputusan merayakan Trisuci ini dinyatakan dalam Konferensi Persaudaraan Buddhis Sedunia (World Fellowship of Buddhists - WFB) yang pertama di Sri Lanka pada tahun 1950. Perayaan ini dilakukan pada purnama pertama di bulan Mei.<br><br>

		<li><a href="https://id.wikipedia.org/wiki/Asadha_Puja" target="_blank">Hari Suci Asadha</a></li>
		<center><img src="../img/Asadha.jpg"> </center> <br>
Asadha Puja (dikenal juga Asalha Puja, Asanha Puja atau Asana Bucha di Thailand, bahasa Thai: วันอาสาฬหบูชา wan-aa-san-ha-boo-chaa) merupakan salah satu perayaan penting bagi umat Buddha terutama aliran Theravada yang biasanya berlangsung di bulan Juli, pada hari bulan purnama di bulan ke-8 penanggalan lunar. Perayaan ini memperingati diberikan khotbah pertama Buddha Gautama di taman rusa Isipatana di Benares dan didirikan sangha di dunia. Di Thailand, Asadha Puja merupakan hari libur pemerintah dan penjualan alkohol dilarang. Asadha sendiri merupakan nama bulan ke-8 dari penanggalan buddhis.<br>
<br>
Biasanya perayaan ini dilakukan dengan mempersembahkan dana ke wihara-wihara dan mendengarkan khotbah-khotbah yang diberikan para biksu. Sehari berikutnya dikenal sebagai bahasa Thai: วันเข้าพรรษา Wan Kao Pansa di Thailand; yang merupakan hari pertama masuknya masa vassa bahasa Thai: พรรษา vassa, masa musim penghujan dimana para biksu atau biksuni harus lebih banyak tinggal di wihara dan melatih praktik-praktik buddhis.<br>
<br>
Di Indonesia, perayaan Asadha berpusat di Wihara Mendut dekat Candi Borobudur.<br><br>

